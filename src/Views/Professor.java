    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = txt_name.getText();
        String last = txt_lastname.getText();

        if(name.isEmpty() || last.isEmpty()){

            JOptionPane.showMessageDialog(this, "Fill the Fields");
        }

        else{

            try{
                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_institucion", "root", "");
                PreparedStatement pst = cn.prepareStatement("insert into professor values(?,?,?,?)");

                pst.setString(1, "1");
                pst.setString(2, txt_name.getText().trim());
                pst.setString(3, txt_lastname.getText().trim());
                pst.setString(4, gender.getSelectedItem().toString());
                pst.executeUpdate();

                JOptionPane.showMessageDialog(this, "Record saved successfully");

                txt_name.setText("");
                txt_lastname.setText("");
                gender.setSelectedIndex(0);
            }catch (Exception e){

            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String search = txt_search.getText();

        if (search.isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter an ID");
        }
        else{
            try {
                String ID = txt_search.getText().trim();

                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_institucion", "root", "");
                PreparedStatement pst = cn.prepareStatement("update professor set name = ?, last_name = ?, gender = ? where ID = " + ID);

                pst.setString(1, txt_name.getText().trim());
                pst.setString(2, txt_lastname.getText().trim());
                pst.setString(3, gender.getSelectedItem().toString());
                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Successful modification");
                txt_name.setText("");
                txt_lastname.setText("");
                gender.setSelectedIndex(0);
                txt_search.setText("");

            } catch (Exception e) {
            }

        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

