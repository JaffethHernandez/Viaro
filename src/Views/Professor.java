    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String search = txt_search.getText();

        if(search.isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter an ID");

        }
        else{

            try{
                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_institucion", "root", "");
                PreparedStatement pst = cn.prepareStatement("select * from professor where ID = ?");
                pst.setString(1, txt_search.getText().trim());

                ResultSet rs = pst.executeQuery();

                if(rs.next()){
                    txt_name.setText(rs.getString("name"));
                    txt_lastname.setText(rs.getString("last_name"));
                    gender.setSelectedItem(rs.getString("gender"));
                } else {
                    JOptionPane.showMessageDialog(null, "Unregistered professor");
                }


            }catch (Exception e){

            }

        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

